#include <stdio.h>

int main(){
    int a = 5, b = 10;
    printf("a < b: %d\n", a < b); //menor que
    printf("a > b: %d\n", a > b); //maior que
    printf("a <= b: %d\n", a <= b); //menor ou igual a
    printf("a >= b: %d\n", a >= b); //maior ou igual a
    printf("a == b: %d\n", a == b); //igual a
    printf("a != b: %d\n", a != b); //diferente de
    return 0;
}